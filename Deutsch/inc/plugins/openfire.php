		"value"			=> 'localhost',
		"disporder"		=> '1',
		"gid"			=> intval($gid),
	);
	
	$openfire_setting_2 = array(
		"sid"			=> "NULL",
		"name"			=> "openfire_secretkey",
		"title"			=> "Openfire Secret Key",
		"description"	=> "Geben Sie hier den Secret Key vom UserService Plugin für Openfire ein.",
